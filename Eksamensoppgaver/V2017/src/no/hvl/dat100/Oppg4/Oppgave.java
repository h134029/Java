package no.hvl.dat100.Oppg4;

public abstract class Oppgave {
    private String sporsmaal;
    private int maksPoeng;

    public Oppgave(String sp, int p) {
        this.sporsmaal = sp;
        this.maksPoeng = p;
    }

    public abstract double poeng(int svar);

    public int getMaksPoeng() {return maksPoeng;}
}
